package neyser;

public class Planeta
{
    // Nombre del planeta
    String nombre;
    // Tipo de planeta
    String tipo;
    // Radio desde el ecuador
    int radio;
    // Constructor
    public Planeta(String nombre, String tipo, int radio)
    {
        this.nombre = nombre;
        this.tipo = tipo;
        this.radio = radio;
    }




}
