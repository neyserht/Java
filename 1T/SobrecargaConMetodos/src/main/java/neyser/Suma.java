package neyser;

public class Suma
{
    public void sumar(int n1, int n2)
    {
        System.out.println(n1+n2);
    };

    public void sumar(double n1, double n2)
    {
        System.out.println(n1+n2);
    };

    public void sumar(int n1, int n2, int n3)
    {
        System.out.println(n1+n2+n3);
    };

    public void sumar(Integer n1, Integer n2)
    {
        System.out.println(n1*n2);
    };
}
